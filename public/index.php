// 定义应用目录
define('APP_PATH', __DIR__ . '/../app/');

// 检查是否安装
if(!is_file(APP_PATH.'install/install.lock')) {
    if (!is_writable(__DIR__ . '/../runtime')) {
        echo '请开启[runtime]文件夹的读写权限';
        exit;
    }
    define('BIND_MODULE', 'install');
}

// 加载框架引导文件
require __DIR__ . '/../thinkphp/start.php';

